package com.spacebank.insurancecenter;

public class YearMatrix {
	Integer year;
	Integer annualPremium;
	Integer claimable;
	Integer lifeInsurance;
	Integer disabilityInsurance;
	Integer policyValue;
	Integer insuredAge;
	
	public Integer getPolicyValue() {
		return policyValue;
	}
	public void setPolicyValue(Integer policyValue) {
		this.policyValue = policyValue;
	}
	public Integer getInsuredAge() {
		return insuredAge;
	}
	public void setInsuredAge(Integer insuredAge) {
		this.insuredAge = insuredAge;
	}
	public Integer getAnnualPremium() {
		return annualPremium;
	}
	public void setAnnualPremium(Integer goalBenefit) {
		this.annualPremium = goalBenefit;
	}
	public Integer getClaimable() {
		return claimable;
	}
	public void setClaimable(Integer claimable) {
		this.claimable = claimable;
	}
	public Integer getLifeInsurance() {
		return lifeInsurance;
	}
	public void setLifeInsurance(Integer lifeInsurance) {
		this.lifeInsurance = lifeInsurance;
	}
	public Integer getDisabilityInsurance() {
		return disabilityInsurance;
	}
	public void setDisabilityInsurance(Integer disabilityInsurance) {
		this.disabilityInsurance = disabilityInsurance;
	}
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}

	
}
